

"""CARACTERES ESPECIALES 
Las cadenas pueden incluir caracteres especiales 
estos caracteres se introducen usando el caracter de diagonal invertida ('\'). Ej 

. Nueva Linea: '\n' -> Inserta un salto de linea 
. Tabulacion: '\t' -> Inserta un tabulador horizontal, util para alinear texto.
. Comilla Simple:'\'' -> Permite incluir comillas simples en una cadena delimitada por comillas simples 
. Comilla Doble: '\''' 
. Barra invertida : '\\'

"""

print('Hola\nMundo') # \n Un salto de linea 

print('\tPython \tes \tgenial') # agrega un tabulador 

print('Juan" \'Perez') # esto agrega una comilla 
print("Karla\" 'Gomez")

print('Caracter de diagonal invertida \\')
